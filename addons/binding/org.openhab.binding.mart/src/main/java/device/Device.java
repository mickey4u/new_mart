package device;

public class Device {
    String deviceName;
    int devicePowerRating;
    int deviceDailyDurationOfUse;

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public int getDevicePowerRating() {
        return devicePowerRating;
    }

    public void setDevicePowerRating(int devicePowerRating) {
        this.devicePowerRating = devicePowerRating;
    }

    public int getDeviceDailyDurationOfUse() {
        return deviceDailyDurationOfUse;
    }

    public void setDeviceDailyDurationOfUse(int deviceDailyDurationOfUse) {
        this.deviceDailyDurationOfUse = deviceDailyDurationOfUse;
    }

}
